#       total would be 410 to 603 + (gen-500) - constant_shift 
# 410 to 603


def count_plants(state,start_idx):
    num = 0
    for i in range(0,len(state)):
        if state[i]=='#':
            print(i)
            num = num+i-start_idx
    return num

